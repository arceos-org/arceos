use std::env;

fn main() {
    println!("cargo:rerun-if-env-changed=AX_CONFIG_PATH");
    if let Ok(config_path) = std::env::var("AX_CONFIG_PATH") {
        println!("cargo:rerun-if-changed={config_path}");
    }

    let task_stack_size = env::var("AX_TASK_STACK_SIZE").unwrap_or("0x40000".into());
    let cpu_num = env::var("AX_CPU_MAX_NUM").unwrap_or("16".into());

    let content = format!(
        r#"///! This file is automatically generated by build.rs. Do not edit it manually.

/// Stack size of each task.
const _TASK_STACK_SIZE: usize = {task_stack_size};
const _CPU_MAX_NUM: usize = {cpu_num}; 
        "#
    );

    let out = std::path::PathBuf::from(std::env::var("OUT_DIR").unwrap()).join("dyn_impl_gen.rs");
    std::fs::write(out, content).unwrap();
}
